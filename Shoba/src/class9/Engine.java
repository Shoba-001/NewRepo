package class9;

public class Engine {
String brand;
int price;
int pistoncount;

public void setBrand(String brand) {
	this.brand=brand;
}
public String getBrand() {
	return brand;
}
public void setPrice(int price) {
	this.price=price;
}
public int getPrice() {
	return price;
}
public void setPistoncount(int pistoncount) {
	this.pistoncount=pistoncount;
}
public int getPistoncount() {
	return pistoncount;
}
}
